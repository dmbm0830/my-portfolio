/**
 * Adds a random fact to the page.
 */
function addRandomFact() {
  const facts =
      ['I have been on two separate game shows!', 'My first experience with Computer Science was at Stanford.',
       'I was stung in the eye by a wasp when I was five years old.', 
       'I have interviewed three former Secretaries of State.'];

  // Pick a random greeting.
  const fact = facts[Math.floor(Math.random() * facts.length)];

  // Add it to the page.
  const factContainer = document.getElementById('fact-container');
  factContainer.innerText = fact;
}

/**
 * Adds a message from the server to the page.
 */
function addServerMessage() {
  fetch('/data').then(response => response.text()).then((message) => {
    document.getElementById('message-container').innerText = message;
  });
}

/**
 * Adds the comments if the user is logged in. Otherwise, prompts a login.
 */
function checkLoginStatus(){
  fetch('/login').then(response => response.text()).then((message) => {
    document.getElementById('message-container').innerHTML = message;
  });
}


